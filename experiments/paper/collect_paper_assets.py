from __future__ import annotations

import argparse
from pathlib import Path

from _paper_utils import ROOT, add_common_args, copy_if_exists, ensure_paper_dirs, write_note


FIGURES = {
    "fig1_cmin_sr_framework.md": None,
    "fig2_spectral_geometry_calibration.png": "outputs/figures/spectral_geometry_calibrator_eval/lambda2_sweep.png",
    "fig3_process_family_map.png": "paper_assets/figures/fig3_process_family_map.png",
    "fig4_mrw_vs_mono_projection.md": None,
    "fig5_finite_sample_identifiability.png": "outputs/figures/finite_sample_identifiability/lambda2_corr_vs_T.png",
    "fig6_zeta_noise_bridge.png": "outputs/figures/zeta_noise_bridge/pmrw_vs_noise.png",
    "fig6_failure_attribution.png": "outputs/figures/cmin_sr_failure_attribution/pmrw_by_level.png",
}

TABLES = {
    "table1_process_family_diagnostics.csv": "outputs/tables/curvature_preserving_zeta_eval/process_by_T_band.csv",
    "table2_mrw_mono_projection.csv": "outputs/tables/cmin_sr_v3_eval/process_by_T.csv",
    "table3_ablation_or_version_comparison.csv": "outputs/tables/cmin_sr_final_version_comparison/version_process_comparison.csv",
    "table4_finite_sample_identifiability.csv": "outputs/tables/finite_sample_identifiability/lambda2_recovery_by_T.csv",
    "table5_real_world_sanity_check.csv": "outputs/tables/raw_market_surrogate_validation/raw_market_surrogate_summary.csv",
}


def main() -> int:
    parser = add_common_args(argparse.ArgumentParser(description="Collect existing outputs into paper_assets."))
    args = parser.parse_args()
    base = ensure_paper_dirs(args.output_dir)
    missing: list[str] = []
    for dst_name, src in FIGURES.items():
        if src is None:
            write_note(
                base / "figures" / dst_name,
                dst_name,
                [
                    "Placeholder note: this figure is conceptual or generated by generate_paper_figures.py.",
                    "Suggested next action: draw the framework/projection diagram for the manuscript.",
                ],
            )
            continue
        if not copy_if_exists(src, base / "figures" / dst_name):
            missing.append(f"Figure missing: {src} -> {dst_name}")
            write_note(
                base / "figures" / f"{Path(dst_name).stem}_missing.md",
                f"Missing {dst_name}",
                [f"Missing source path: {ROOT / src}", "Generate the corresponding experiment or run generate_paper_figures.py."],
            )
    for dst_name, src in TABLES.items():
        if not copy_if_exists(src, base / "tables" / dst_name):
            missing.append(f"Table missing: {src} -> {dst_name}")
            write_note(
                base / "tables" / f"{Path(dst_name).stem}_missing.md",
                f"Missing {dst_name}",
                [f"Missing source path: {ROOT / src}", "Generate the corresponding experiment first."],
            )
    lines = [
        "Collected available paper assets from existing outputs.",
        f"Output directory: {base}",
        "",
        "Missing assets are non-fatal; generate the corresponding experiment and rerun collection.",
    ]
    if missing:
        lines.extend(["", "Missing:"])
        lines.extend(f"- {m}" for m in missing)
    else:
        lines.append("No missing configured assets.")
    write_note(base / "summaries" / "asset_collection_manifest.md", "Paper Asset Collection", lines)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
